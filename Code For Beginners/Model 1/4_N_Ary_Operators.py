"""Unary"""
N = 10
print(-N)

'''Relational'''

print("Check 10 > 50 = " + str(10 > 50))
print("Check 10 > 5 = " + str(10 > 5))
print("Check 10 = 5 = " + str(10 == 5))
print("Check 10 >= 10 = " + str(10 >= 10))
print("Check 10 != 10 = " + str(10 != 10))

'''Logical'''

print("ANDing - " + str(True and False))
print("ORing - " + str(True or False))
print("Not True - " + str(not True))
